// Function to set memo details in local storage
function setMemoDetails(memo) {
    localStorage.setItem('memodetails', memo);
}

// Function to get memo details from local storage
function getMemoDetails() {
    return localStorage.getItem('memodetails');
}

// Function to clear memo details from local storage
function clearMemoDetails() {
    localStorage.removeItem('memodetails');
}

